public class tempoEmSegundos {
    public static void main(String[] args) {
        int segundos = 30;
        int minutos = 15;
        int horas = 2;
        int totalSegundos= (horas*3600)+(minutos*60)+segundos;
        System.out.println(totalSegundos);
    }
}
